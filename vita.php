		<dt><strong>On the Support Properties of Scaling Vectors</strong>, with P. Massopust and D. Ruch, <i>J. App. 		and Comp. Harmonic Anal.</i>, <strong>3</strong>(1996), 229-238.</dt>
		<dd><a href="javascript:toggleLayer('<?php echo($blurbid)?>')">Abstract</a> | 
