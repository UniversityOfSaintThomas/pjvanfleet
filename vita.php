		<p></p>
		We report an extension of a result due to M. Daehlen [8] whereby an s-variate box spline can be evaluated using 		-1)-variate simplex splines.  In order to derive his algorithm, Daehlen employed a result of Cohen, Lyche, and 		Riesenfeld [7] that illustrates how an s-variate cone spline can be expressed as a combination of (s-1)-variate 		simplex splines.  The result of [7] defines a special linear map \pi : <strong>R</strong><sup>s</sup> to 			<strong>R</strong> that depends on a nonsingular matrix V.  In order for the algorithm to succeed, Daehlen 			requires the condition that V must be chosen so that \pi(x<sup>i</sup>) > 0 for each of the box spline knot  		points x<sup>i</sup>, i=1,...,n.  We report a method for constructing V that satisfies the condition \pi(x<sup>		i</sup>) > 0.  We then discuss the implementation of our result Daehlen's algorithm.
		</span></dd>
