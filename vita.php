		<dt><strong>On the Evaluation of Simplicial Splines</strong>, <i>Proceedings of CAM*97</i>, University of 			Central Oklahoma, 1997, 314-328.</dt>
		<dd><a href="javascript:toggleLayer('<?php echo($blurbid)?>')">Abstract</a> |
