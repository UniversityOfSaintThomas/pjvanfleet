		<p></p>
		We consider the problem of large-scale evaluation of multivariate simplicial splines.  These splines arise 			naturally as the multivariate analog of B-splines and are useful in applications such as finite element 			methods and computer-aided geometric design (CAGD).  Simplicial splines have much in common with their 			univariate analog.  They are piecewise polynomial functions whose smoothness and support can be controlled by 		knot placement.  In addition, these splines obey a recurrence formula.  Unlike the univariate case, 				implementation of this formula poses some problems.  If x in <strong>R</strong><sup>s</sup> lies on an (s-1)-		dimensional hyperplane <i>H</i> connecting two or more knot points, then the recurrence formula cannot be 			utilized.
		<p></p>
		Results on the evaluation of multivariate simplicial splines are documented in [7,10].  In both cases, the 			authors use perturbation techniques when faced with evaluating the spline at x in <i>H</i>.  In [7], the author 		suggests a stable means for evaluating simplicial splines at points on the interior of regions formed by (s-1)		-dimensional hyperplanes and we augment this method with a scheme for evaluating points on any such hyperplane.   		Our method is especially suited to large-scale evaluation of the spline and it is numerically stable when the 		evaluation is performed near or on (s-1)-dimensional hyperplanes.  Examples are given to demonstrate the
		algorithm.
		</span></dd>
