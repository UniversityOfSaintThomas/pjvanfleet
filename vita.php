		<p></p>	
		In many applications of image processing, the given data are integer-valued.  It is therefore desirable to 			construct transformations that map data of this type to an integer (or rational) ring.  Calderbank,
		Daubechies, Sweldens, and Yeo [1] devised two methods for modifying orthogonal and biorthonal wavelets so that 		they map integers to integers.
		<p></p>
		The first method involves appropriately scaling the transform so that data that has been transformed and 			truncated can be recovered via the inverse wavelet transform.  In developing this method, the authors of
		[1] created a useful factorization of the 4-tap Daubechies orthogonal wavelet transform [2].  We have observed 		that this factorization can be extended to 4-tap multiwavelets of arbitrary size.
		<p></p>
		In this paper we will discuss this generalization and illustrate the factorization on two multiwavelets.  In 		particular, the well-known Donovan, Geronimo, Hardin, and Massopust (DGHM) [3] multiwavelet transform can be 		scaled so that it maps integers to integers.  Since this transform is (anti)symmetric in addition to 				orthogonal, regular, and compactly supported, the ability to modify it so that it maps integers to integers 		should be useful in image processing applications.
		</span></dd>
