		<dd><a href="javascript:toggleLayer('<?php echo($blurbid)?>')">Abstract</a> | 
		<a href="pdffiles/gibbs.pdf" Title="Gibbs Paper">PDF File</a>
		<span class="posthidden" id="<?php echo($blurbid)?>">
		<?php $blurbid+=1 ?>
		<p></p>
		This paper considers Gibbs' phenomenon for scaling vectors in L<sup>2</sup>(<strong>R</strong>). We first show 		that a wide class of multiresolution analyses suffer from Gibbs' phenomenon.
		<p></p>
		To deal with this problem, in [11], Walter and Shen use an Abel summation technique to construct a positive 		scaling  function P<sub>r</sub>, 0 < r < 1, from an orthonormal scaling function $\phi$ that generates  V<sub>0			</sub>.  A reproducing kernel can in turn be constructed using P<sub>r</sub>.  This kernel is  also positive, 		has unit integral, and approximations utilitizing it display no Gibbs' phenomenon.  These results were extended 		to scaling vectors and multiwavelets in [9].  In both cases, orthogonality and compact support were lost in the 		construction process.
		<p></p>
		In this paper we modify the approach given in [9] to construct compactly  supported positive scaling vectors. 		While the mapping into V<sub>0</sub> associated with this new positive scaling vector is not a projection, the 		scaling vector does produce a Riesz basis for V<sub>0</sub> and we conclude  the paper by illustrating
		that expansions of functions via positive scaling vectors exhibit no Gibbs' phenomenon.
		</span></dd>
