		<p></p>
		In Chui and Wang [3], support properties are derived for a scaling function generating a function space 			V<sub>0</sub> in L<sup>2</sup>(<strong>R</strong>).  Motivated by this work, we consider support properties for 		scaling vectors.  In [9], Goodman and Lee derive necessary and sufficient conditions for the scaling
		vector {\phi_1,...., \phi_r}, r > 0, to form a Riesz basis for V<sub>0</sub> and develop a general theory for 		spline wavelets of multiplicity r > 1.  We consider conditions under which linear combinations of scaling 			functions generate V<sub>0</sub>.  These conditions also characterize all other scaling vectors that generate 		the same V<sub>0</sub>.  In addition we describe the scaling vectors of minimal support for V<sub>0</sub>.
		<p></p>
		Next, we give sufficient conditions on the two-scale symbol for scaling vectors under which a given matrix 			refinement equation can be solved.  A spline-wavelet example illustrates these results.
		<p></p>
		For the single scaling function \phi, the support of \phi is characterized by the degree of the two-scale 			symbol.  The situation is more complicated in the scaling vector case.  We prove a result that gives the 		 	support of the scaling vector under certain conditions on the coefficient matrices.  This result is illustrated 		by an example of fractal wavelets derived in Geronimo, Hardin, and Massopust [8].
