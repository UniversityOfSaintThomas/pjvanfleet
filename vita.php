		<dt><strong>An Algorithm for the Evaluation of Box Splines</strong>, <i> Proceedings of the CAM*95</i>, 			University of Central Oklahoma, 1995, 269-282.</dt>
		<dd><a href="javascript:toggleLayer('<?php echo($blurbid)?>')">Abstract</a>
